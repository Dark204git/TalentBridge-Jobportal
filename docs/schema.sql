-- ── Extensions ───────────────────────────────────────────────
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS vector;          -- pgvector for AI embeddings


-- ============================================================
--  TABLE: users
--  Central auth/identity record for every account.
-- ============================================================
CREATE TABLE IF NOT EXISTS users (
  id            UUID         PRIMARY KEY DEFAULT uuid_generate_v4(),
  email         VARCHAR(255) UNIQUE NOT NULL,
  password_hash VARCHAR(255) NOT NULL,
  full_name     VARCHAR(255) NOT NULL,
  role          VARCHAR(20)  NOT NULL CHECK (role IN ('candidate', 'employer', 'admin')),
  is_active     BOOLEAN      NOT NULL DEFAULT TRUE,
  created_at    TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at    TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);


-- ============================================================
--  TABLE: employer_profiles
--  Company details for employer accounts (1-to-1 with users).
-- ============================================================
CREATE TABLE IF NOT EXISTS employer_profiles (
  id                  UUID         PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id             UUID         NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  company_name        VARCHAR(255),
  company_description TEXT,
  industry            VARCHAR(100),
  company_size        VARCHAR(50),
  company_website     VARCHAR(255),
  company_logo        TEXT,
  headquarters        VARCHAR(255),
  founded_year        INTEGER,
  linkedin_url        VARCHAR(255),
  twitter_url         VARCHAR(255),
  created_at          TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at          TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  UNIQUE (user_id)
);


-- ============================================================
--  TABLE: candidate_profiles
--  Full profile for job-seeker accounts (1-to-1 with users).
--  The embedding column stores a 384-dim sentence vector
--  generated by all-MiniLM-L6-v2 for AI job matching.
-- ============================================================
CREATE TABLE IF NOT EXISTS candidate_profiles (
  id                  UUID         PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id             UUID         NOT NULL REFERENCES users(id) ON DELETE CASCADE,

  -- Personal details
  headline            VARCHAR(255),
  bio                 TEXT,
  profile_picture     TEXT,
  phone_number        TEXT,
  country_code        TEXT         NOT NULL DEFAULT '+91',
  date_of_birth       DATE,
  gender              TEXT,
  lives_in            TEXT,

  -- Professional details
  skills              TEXT[]       NOT NULL DEFAULT '{}',
  experience_years    INTEGER,
  education           TEXT,
  experience          TEXT,
  desired_job_title   VARCHAR(255),
  preferred_location  VARCHAR(255),
  preferred_category  VARCHAR(100),
  preferred_job_type  VARCHAR(50),
  desired_salary      TEXT,
  is_open_to_work     BOOLEAN      NOT NULL DEFAULT TRUE,

  -- Resume
  resume_url          TEXT,
  resume_key          TEXT,
  resume_filename     VARCHAR(255),
  resume_uploaded_at  TIMESTAMPTZ,
  resume_parsed       BOOLEAN      NOT NULL DEFAULT FALSE,
  resume_parsed_at    TIMESTAMPTZ,
  resume_parse_failed BOOLEAN      NOT NULL DEFAULT FALSE,

  -- Social links
  linkedin_url        VARCHAR(255),
  github_url          VARCHAR(255),
  portfolio_url       VARCHAR(255),

  -- AI matching vector (384-dim cosine)
  embedding           VECTOR(384),

  created_at          TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at          TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  UNIQUE (user_id)
);


-- ============================================================
--  TABLE: jobs
--  Job listings posted by employers.
--  embedding is generated at post time for AI candidate matching.
-- ============================================================
CREATE TABLE IF NOT EXISTS jobs (
  id                   UUID         PRIMARY KEY DEFAULT uuid_generate_v4(),
  employer_id          UUID         NOT NULL REFERENCES users(id)            ON DELETE CASCADE,
  employer_profile_id  UUID         REFERENCES employer_profiles(id)         ON DELETE SET NULL,

  -- Content
  title                VARCHAR(255) NOT NULL,
  description          TEXT         NOT NULL,
  requirements         TEXT,
  skills               TEXT[]       NOT NULL DEFAULT '{}',

  -- Classification
  location             VARCHAR(255),
  is_remote            BOOLEAN      NOT NULL DEFAULT FALSE,
  job_type             VARCHAR(50)  CHECK (job_type IN (
                          'full-time', 'part-time', 'contract', 'freelance', 'internship'
                        )),
  experience_level     VARCHAR(50)  CHECK (experience_level IN (
                          'entry', 'mid', 'senior', 'lead', 'executive'
                        )),
  category             VARCHAR(100),

  -- Compensation
  salary_min           INTEGER,
  salary_max           INTEGER,

  -- Lifecycle
  status               VARCHAR(20)  NOT NULL DEFAULT 'active' CHECK (status IN ('active', 'closed', 'draft')),
  application_deadline TIMESTAMPTZ,
  views                INTEGER      NOT NULL DEFAULT 0,

  -- AI matching vector (384-dim cosine)
  embedding            VECTOR(384),

  created_at           TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at           TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);


-- ============================================================
--  TABLE: applications
--  Tracks every candidate ↔ job application and its status.
-- ============================================================
CREATE TABLE IF NOT EXISTS applications (
  id             UUID         PRIMARY KEY DEFAULT uuid_generate_v4(),
  job_id         UUID         NOT NULL REFERENCES jobs(id)  ON DELETE CASCADE,
  candidate_id   UUID         NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  employer_id    UUID         NOT NULL REFERENCES users(id) ON DELETE CASCADE,

  cover_letter   TEXT,
  resume_url     TEXT,

  status         VARCHAR(30)  NOT NULL DEFAULT 'pending' CHECK (
                   status IN ('pending', 'reviewing', 'shortlisted',
                              'interviewed', 'offered', 'rejected', 'withdrawn')
                 ),
  employer_notes TEXT,
  match_score    INTEGER,

  applied_at     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),

  UNIQUE (job_id, candidate_id)
);


-- ============================================================
--  TABLE: saved_jobs
--  Bookmarked jobs per candidate.
-- ============================================================
CREATE TABLE IF NOT EXISTS saved_jobs (
  id       UUID        PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id  UUID        NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  job_id   UUID        NOT NULL REFERENCES jobs(id)  ON DELETE CASCADE,
  saved_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (user_id, job_id)
);


-- ============================================================
--  TABLE: job_views
--  Per-impression analytics for each job listing.
-- ============================================================
CREATE TABLE IF NOT EXISTS job_views (
  id        UUID        PRIMARY KEY DEFAULT uuid_generate_v4(),
  job_id    UUID        NOT NULL REFERENCES jobs(id)  ON DELETE CASCADE,
  viewer_id UUID        REFERENCES users(id)          ON DELETE SET NULL,
  viewed_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);


-- ============================================================
--  TABLE: password_reset_tokens
--  Short-lived SHA-256 hashed tokens for password resets.
-- ============================================================
CREATE TABLE IF NOT EXISTS password_reset_tokens (
  id         UUID        PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id    UUID        NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  token_hash TEXT        NOT NULL UNIQUE,
  expires_at TIMESTAMPTZ NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);


-- ============================================================
--  TABLE: notifications
--  In-app + email notification records per user.
--  type values: application_received | application_status_changed |
--               job_match | job_saved | profile_viewed | system
-- ============================================================
CREATE TABLE IF NOT EXISTS notifications (
  id         UUID         PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id    UUID         NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  type       VARCHAR(50)  NOT NULL,
  title      VARCHAR(255) NOT NULL,
  message    TEXT         NOT NULL,
  link       VARCHAR(500),
  is_read    BOOLEAN      NOT NULL DEFAULT FALSE,
  created_at TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);


