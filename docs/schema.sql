-- jobs_with_company: active jobs enriched with employer details.
CREATE OR REPLACE VIEW jobs_with_company AS
SELECT
  j.*,
  ep.company_name,
  ep.company_logo,
  ep.company_website,
  ep.industry
FROM  jobs j
LEFT JOIN employer_profiles ep ON ep.id = j.employer_profile_id
WHERE j.status = 'active';


-- job_application_stats: aggregated funnel metrics per job.
CREATE OR REPLACE VIEW job_application_stats AS
SELECT
  j.id                                                         AS job_id,
  j.title,
  j.employer_id,
  j.views,
  COUNT(a.id)                                                  AS total_applications,
  COUNT(a.id) FILTER (WHERE a.status = 'shortlisted')         AS shortlisted_count,
  COUNT(a.id) FILTER (WHERE a.status = 'interviewed')         AS interviewed_count,
  COUNT(a.id) FILTER (WHERE a.status = 'offered')             AS offered_count,
  COUNT(a.id) FILTER (WHERE a.status = 'rejected')            AS rejected_count
FROM  jobs j
LEFT JOIN applications a ON a.job_id = j.id
GROUP BY j.id, j.title, j.employer_id, j.views;


-- ============================================================
--  FUNCTION: match_jobs_for_candidate
--  Called by matchingService.js → findMatchingJobsForCandidate()
--
--  Given a candidate's 384-dim embedding, returns up to
--  match_count active jobs ranked by cosine similarity.
--  match_threshold = 0.0 by default – the composite scoring
--  layer in Node.js is the real quality gate.
-- ============================================================
DROP FUNCTION IF EXISTS match_jobs_for_candidate (vector, float, int);

CREATE FUNCTION match_jobs_for_candidate (
  query_embedding  vector(384),
  match_threshold  float   DEFAULT 0.0,
  match_count      integer DEFAULT 20
)
RETURNS TABLE (
  id                  uuid,
  title               text,
  description         text,
  requirements        text,
  skills              text[],
  location            text,
  job_type            text,
  experience_level    text,
  category            text,
  salary_min          integer,
  salary_max          integer,
  is_remote           boolean,
  status              text,
  employer_id         uuid,
  employer_profile_id uuid,
  views               integer,
  created_at          timestamptz,
  updated_at          timestamptz,
  similarity          float
)
LANGUAGE sql STABLE
AS $$
  SELECT
    j.id,
    j.title,
    j.description,
    j.requirements,
    j.skills,
    j.location,
    j.job_type,
    j.experience_level,
    j.category,
    j.salary_min,
    j.salary_max,
    j.is_remote,
    j.status,
    j.employer_id,
    j.employer_profile_id,
    j.views,
    j.created_at,
    j.updated_at,
    (1 - (j.embedding <=> query_embedding))::float AS similarity
  FROM  jobs j
  WHERE j.status    = 'active'
    AND j.embedding IS NOT NULL
    AND (1 - (j.embedding <=> query_embedding)) >= match_threshold
  ORDER BY j.embedding <=> query_embedding   -- cosine distance ASC = similarity DESC
  LIMIT match_count;
$$;


-- ============================================================
--  FUNCTION: match_candidates_for_job
--  Called by matchingService.js → findMatchingCandidatesForJob()
--              and triggerJobMatchingOnPost()
--
--  Given a job's 384-dim embedding, returns up to match_count
--  open-to-work candidates ranked by cosine similarity.
-- ============================================================
DROP FUNCTION IF EXISTS match_candidates_for_job (vector, float, int);

CREATE FUNCTION match_candidates_for_job (
  query_embedding  vector(384),
  match_threshold  float   DEFAULT 0.0,
  match_count      integer DEFAULT 30
)
RETURNS TABLE (
  user_id            uuid,
  headline           text,
  bio                text,
  skills             text[],
  experience_years   integer,
  preferred_location text,
  resume_url         text,
  similarity         float
)
LANGUAGE sql STABLE
AS $$
  SELECT
    cp.user_id,
    cp.headline,
    cp.bio,
    cp.skills,
    cp.experience_years,
    cp.preferred_location,
    cp.resume_url,
    (1 - (cp.embedding <=> query_embedding))::float AS similarity
  FROM  candidate_profiles cp
  WHERE cp.embedding      IS NOT NULL
    AND cp.is_open_to_work = TRUE
    AND (1 - (cp.embedding <=> query_embedding)) >= match_threshold
  ORDER BY cp.embedding <=> query_embedding   -- cosine distance ASC = similarity DESC
  LIMIT match_count;
$$;


