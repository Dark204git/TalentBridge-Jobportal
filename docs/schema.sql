-- ============================================================
--  INDEXES  –  HNSW vector (approximate nearest-neighbour)
--  Uses cosine distance to match the <=> operator in the
--  RPC functions below.
--  m=16 / ef_construction=64 is a good default for this scale.
-- ============================================================

CREATE INDEX IF NOT EXISTS idx_jobs_embedding_hnsw
  ON jobs USING hnsw (embedding vector_cosine_ops)
  WITH (m = 16, ef_construction = 64);

CREATE INDEX IF NOT EXISTS idx_candidates_embedding_hnsw
  ON candidate_profiles USING hnsw (embedding vector_cosine_ops)
  WITH (m = 16, ef_construction = 64);


